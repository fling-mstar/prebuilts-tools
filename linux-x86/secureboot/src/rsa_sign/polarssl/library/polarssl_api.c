U8 ASCII2HEX( char* Ascii )
{
    U8 ret = 0;

    if ( ( *Ascii < 0x3A ) && ( *Ascii > 0x2F ) )
        ret = *Ascii - 0x30;
    else if ( ( *Ascii < 0x47 ) && ( *Ascii > 0x40 ) )
        ret = *Ascii - 0x30 - 0x7;
    else if ( ( *Ascii < 0x67 ) && ( *Ascii > 0x60 ) )
        ret = *Ascii - 0x30 - 0x27;

    return ret;
}

